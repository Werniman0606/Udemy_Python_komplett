def run_quiz(fragen):
    """
    F√ºhrt das Schulbus (S) Endorsement Quiz durch.
    """
    falsche_antworten = 0
    gesamt_fragen = len(fragen)
    # Da die Optionen entweder 2 (Wahr/Falsch), 3 oder 4 sind
    antwort_mapping = {'A': 0, 'B': 1, 'C': 2, 'D': 3}
    optionen_labels = ['A', 'B', 'C', 'D']

    print("=== CDL School Bus (S) Endorsement Practice Test Quiz ===")
    print(f"Es warten {gesamt_fragen} Fragen auf dich.")
    print("Bitte w√§hle die korrekte Antwort (A, B, C oder D) f√ºr jede Frage. Tippe 'EXIT' zum Beenden.")
    print("-" * 40)

    for i, (frage, optionen, korrekter_index) in enumerate(fragen, 1):
        aktuelle_labels = optionen_labels[:len(optionen)]

        print(f"\nFrage {i}: {frage}")
        for label, option in zip(aktuelle_labels, optionen):
            print(f"  {label}. {option}")

        while True:
            try:
                benutzer_eingabe = input("Deine Antwort: ").strip().upper()

                if benutzer_eingabe == 'EXIT':
                    print("\nQuiz vorzeitig beendet.")
                    print("-" * 40)
                    print(f"Ergebnis bis hierhin: {i - 1} Fragen beantwortet, {falsche_antworten} falsch.")
                    return

                if benutzer_eingabe in aktuelle_labels:
                    benutzer_index = antwort_mapping[benutzer_eingabe]
                    break
                else:
                    print(f"Ung√ºltige Eingabe. Bitte gib {', '.join(aktuelle_labels)} ein.")
            except:
                print("Ein Fehler ist aufgetreten. Bitte versuche es erneut.")

        # Antwort pr√ºfen
        if benutzer_index == korrekter_index:
            print("‚úÖ Korrekt!")
        else:
            falsche_antworten += 1
            korrekte_antwort_label = optionen_labels[korrekter_index]
            korrekte_antwort_text = optionen[korrekter_index]
            print(f"‚ùå Falsch. Die richtige Antwort ist **{korrekte_antwort_label}**: {korrekte_antwort_text}")

        print("-" * 40)

    # Zusammenfassung
    print("\n=== Quiz beendet ===")
    print(f"Du hast insgesamt {gesamt_fragen} Fragen beantwortet.")
    richtige_antworten = gesamt_fragen - falsche_antworten
    print(f"Anzahl der **richtigen** Antworten: {richtige_antworten} / {gesamt_fragen}")
    print(f"Anzahl der **falschen** Antworten: {falsche_antworten} / {gesamt_fragen} üôÅ")


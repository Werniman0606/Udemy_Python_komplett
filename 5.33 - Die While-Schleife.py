counter = 0
while counter < 10:  # solange die Variante counter kleiner als 10 ist
    print(counter)  # gib den aktuellen Wert aus
    counter = counter + 1  # und zähle die Variable um 1 hoch
# Ausgabe 0 bis 9,

"""Das heißt, die Whileschleife läuft so lange, bis die Bedingung, die hinter dem While steht, erfüllt ist. Vergisst man, eine Ausstiegsfunktion einzubauen (z.B. das hochzählen 
um 1), würde die Schleife unendlich laufen."""


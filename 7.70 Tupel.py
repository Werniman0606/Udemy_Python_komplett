# Ein Tupel ist ähnlich einer Liste, wird aber für andere Zwecke verwendet
t = (1,2,3) # Hurra, wir haben unseren ersten Tupel erzeugt
print(t) # und er wird sogleich ausgegeben

# Unterschied zur Liste
# Eine Liste ist mutable,d.h. veränderlich,so dass man mit dem Befehl liste.append(5) einen Eintrag hinzufügen kann.
# Bei einem Tupel funktioniert das nicht, es würde einen Typerror geben. Er ist immutable.


